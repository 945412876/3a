public class switchdemo1 {
    public static void main(String[] args) {
        String weekday ="3";
        switch(weekday){
            case"1":
                System.out.println("aaaaaa");
                break;
            case"2":
                System.out.println("bbbbbbbbb");
                break;
            case"3" :
                System.out.println("ccccccccccccc");
                break;


        }
    }
}
